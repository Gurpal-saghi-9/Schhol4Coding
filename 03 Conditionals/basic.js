/*
A Conditional statement lets your code make decisions. It checks if something is true orfalse, and then runs certain code based on that.

Syntax:
if (condition) {
    // code to run if the condition is true
} else {
    // code to run if the condition is false
}

Example:
"If it's raining, take an umbrella. Otherwise, enjoy the sunshine!"
*/

let wheather = "rainy";

if (wheather === "rainy") {
    console.log("Take an umbrella");
} else {
    console.log("Enjoy the sunshine!");
}

/* 
Different typpes of Conditional Statements:
1. if statement
2. if...else statement
3. if...else if...else statement
4. Nested if statement
5. switch statement
6. Ternary Operator [?:] (short form)
*/