// Comparison Operators:- These help you compare values. They give true or false

let x = 5;
let y = '5';

// Equal to (==) operator
console.log(x == y); // Outputs: true (type coercion)

// Strict Equal to (===) operator
console.log(x === y); // Outputs: false (no type coercion)

// Not Equal to (!=) operator
console.log(x != y); // Outputs: false (type coercion)

// Strict Not Equal to (!==) operator
console.log(x !== y); // Outputs: true (no type coercion)

// Greater than (>) operator
console.log(x > 3); // Outputs: true

// Less than (<) operator
console.log(x < 10); // Outputs: true

// Greater than or Equal to (>=) operator
console.log(x >= 5); // Outputs: true

// Less than or Equal to (<=) operator
console.log(x <= 5); // Outputs: true

