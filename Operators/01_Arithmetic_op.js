// Addition(+)

let n1 = 5, n2 = 2;
console.log(n1 + n2); 

// Subtraction(-)
console.log(n1 - n2); 

// Multiplication(*)
console.log(n1 * n2); 

// Division(/)
console.log(n1 / n2); 

// Modulus(%)
console.log(n1 % n2); // 1 (remainder of 10 divided by 3)

// Exponentiation(**)
console.log(n1 ** n2); // 8 (2 raised to the power of 3)

